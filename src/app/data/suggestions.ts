/* ─── Helpers ─────────────────────────────────────────────────────────────── */

/** Renvoie les hashtags correspondant à la query (sans #) */
export function searchHashtags(query: string, max = 6): string[] {
  if (!query) return HASHTAGS.slice(0, max);
  const q = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  return HASHTAGS.filter((h) =>
    h.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").includes(q)
  ).slice(0, max);
}

/** Renvoie les utilisateurs correspondant à la query (sans @) */
export function searchUsers(query: string, max = 5): typeof USERS {
  if (!query) return USERS.slice(0, max);
  const q = query.toLowerCase();
  return USERS.filter(
    (u) => u.handle.toLowerCase().includes(q) || u.name.toLowerCase().includes(q)
  ).slice(0, max);
}
